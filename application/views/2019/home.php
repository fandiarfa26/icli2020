	<h1 class="my-4">Welcome to ICLI 2019</h1>
	ICLI is an annual International Conference on Learning Innovation (ICLI) hosted by Universitas Negeri Malang,
	Indonesia in collaboration with the Islamic Development Bank (IsDB) and Indonesian Consortium for Learning
	Innovation Research (ICLIR). It aims to gather researchers, practitioners, students, experts, consultants, teachers
	and lecturers to share their insights and experiences on research not only in constructing innovations in learning
	but also the knowledge of learner’s capability. The learners who are characterized as creative and competent by
	having the ability to understand what they have learned and capable of taking initiative and thinking critically. In
	addition, ICLI is organized on the basis of the trend in the 21<sup>st</sup> century, categorized by the increasing
	complexity of technology and the emergence of a corporate restructuring movement. </p>
	<p>
		The aim of the conference is to provide a platform to the researchers, experts and practitioners from academia,
		governments, NGOs, research institutes, and industries to meet and share cutting-edge progress in the fields of
		Education.
	</p>
	<p>
		This third ICLI will be held on October 9-10, 2019. The big theme for this year would be “Disruptive Learning
		Innovation”. This big theme has some subdivisions which hopefully brings the new development related to
		Industrial Era 4.0 into our system of education. This way, our university are prepared for giving the young
		generation brighter future.
	</p>


